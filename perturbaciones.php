        <section id="naturales">
            <h2><center>Perturbaciones Naturales</center></h2>
            <p>Las perturbaciones naturales son aquellos eventos o procesos que alteran un ecosistema sin intervención directa del ser humano. Estos eventos pueden variar en escala, desde fenómenos locales como incendios forestales hasta eventos globales como erupciones volcánicas y terremotos.</p>
                <p><b>Causas:</b>
                    Las perturbaciones naturales son el resultado de procesos geológicos, climáticos y biológicos. Por ejemplo, los incendios forestales pueden ser causados por rayos, los terremotos por el movimiento de las placas tectónicas y las erupciones volcánicas por la actividad interna de la Tierra.</p>
                <p><b>Ejemplos:</b>
                Además de los mencionados, otros ejemplos de perturbaciones naturales incluyen inundaciones, sequías, huracanes, deslizamientos de tierra y cambios en los patrones climáticos a largo plazo.
                </p>
                <p><b>Presentacion en la vida diaria:</b>
                Las perturbaciones naturales son parte integral de la vida en la Tierra y han moldeado los ecosistemas durante millones de años. Sin embargo, sus efectos pueden ser devastadores para las comunidades humanas y los ecosistemas, causando pérdidas de vidas, daños a la infraestructura y alteraciones en los ciclos biogeoquímicos.</p>
        </section>
        <section id="antropicas">
            <h2><center>Perturbaciones Antropicas</center></h2>
            <p>Las perturbaciones antrópicas son cualquier acción o proceso originado por el ser humano que altera la estructura, función o composición de un ecosistema. Estas alteraciones pueden ser directas, como la deforestación o la contaminación, o indirectas, como el cambio climático inducido por las emisiones de gases de efecto invernadero.</p>
            <p><b>Ejemplos:</b></p>
            <p><b>•	Deforestación:</b> La tala indiscriminada de árboles para obtener madera, ampliar áreas agrícolas o urbanizar destruye hábitats y reduce la biodiversidad.</p>
            <p><b>•	Contaminación:</b> La liberación de sustancias tóxicas en el aire, el agua y el suelo afecta a los organismos vivos y altera los ciclos biogeoquímicos.</p>
            <p><b>•	Cambio climático:</b> El aumento de las temperaturas globales, los cambios en los patrones de precipitación y el aumento del nivel del mar son consecuencias directas de las emisiones de gases de efecto invernadero.</p>
            <p><b>•	Introducción de especies invasoras:</b> La introducción accidental o intencional de especies exóticas en nuevos ecosistemas puede causar la extinción de especies nativas y alterar las redes tróficas.</p>
            <p><b>•	Urbanización:</b> La expansión de las ciudades implica la destrucción de hábitats naturales, la fragmentación de los ecosistemas y la generación de grandes cantidades de residuos.</p>
            <p><b>•	Sobreexplotación de recursos naturales:</b> La sobreexplotación es de origen humano y se concreta extrayendo individuos de una especie vegetal o animal no humana al punto de poner en peligro su reproducción, o utilizando los ecosistemas a una tasa mayor a la de su regeneración natural.</p>
        </section>
